package poo.interfases;

public class Empleado {
    //parametros
    String nombre;
    int edad;
    double salario;
    boolean alta;
    
    //constructores
    public Empleado() {}
    
    public Empleado(String nombre, int edad, double salario, boolean alta) {
        this.nombre = nombre;
        this.edad = edad;
        this.salario = salario;
        this.alta = alta;
    }
    


    
}
